# Vocabulary size (unique terms across all vectors)
all_terms = set()
for v in model.vectors:
    all_terms.update(v.keys())
vocab_size = len(all_terms)

# Speed test — time one recommendation call
t0 = time.perf_counter()
test_recs = model.recommend("The Godfather", limit=5)
elapsed_ms = (time.perf_counter() - t0) * 1000

